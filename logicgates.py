num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("num1 AND num2 = ", num1 & num2)

print("num1 OR num2 = ", num1 | num2)

print("NOT num1 = ", ~num1)
print("NOT num2 = ", ~num2)

print("num1 XOR num2 = ", num1 ^ num2)

print("Left shift 3 of num1 = ", num1 << 3)
print("Left shift 3 of num2 = ", num2 << 3)

print("Right shift 4 of num1 = ", num1 >> 4)
print("Right shift 4 of num2 = ", num2 >> 4)
